export type TranslationKey = keyof typeof translations.es;

interface LanguageContextType {
  lang:    Language;
  setLang: (l: Language) => void;
  t:       (key: TranslationKey) => string;
}

const LanguageContext = createContext<LanguageContextType>({
  lang:    LANGUAGES[0],
  setLang: () => {},
  t:       (key) => key,
});

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Language>(LANGUAGES[0]);

  useEffect(() => {
    const stored = localStorage.getItem("lang") as LangCode | null;
    if (stored) {
      const found = LANGUAGES.find((l) => l.code === stored);
      if (found) setLangState(found);
    }
  }, []);

  const setLang = (l: Language) => {
    setLangState(l);
    localStorage.setItem("lang", l.code);
    document.documentElement.lang = l.code;
  };

  const t = (key: TranslationKey): string =>
    translations[lang.code][key] ?? translations.es[key] ?? key;

  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  return useContext(LanguageContext);
}
